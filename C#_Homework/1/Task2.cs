namespace _1
{
    /// <summary>
    /// Задача 2: Константы
    /// Определите несколько констант в своем коде (например, число Пи, число дней в неделе) 
    /// и выведите их значения на экран. Объясните, почему вы используете константы.
    /// </summary>
    public class Task2
    {
        // Константы объявляются с ключевым словом const
        // Они не могут быть изменены после инициализации
        // Зачем использовать константы:
        // 1. Защита от случайного изменения значений
        // 2. Улучшение читаемости кода (осмысленные имена вместо магических чисел)
        // 3. Централизованное управление значениями (легче изменять в одном месте)
        // 4. Предотвращение ошибок при опечатках
        // 5. Компилятор может оптимизировать код, используя константы
        private const double PI = 3.14159265359;           // Число Пи (π)
        private const double FI = 1.618033988749;          // Золотое сечение (φ)
        private const double E = 2.718281828459;           // Число Эйлера (e)

        public static void Run()
        {
            Console.WriteLine("=== Задача 2: Константы ===\n");

            // Вывод значений констант
            Console.WriteLine($"Число Пи (π): {PI}");
            Console.WriteLine($"Золотое сечение (φ): {FI}");
            Console.WriteLine($"Число Эйлера (e): {E}");

            Console.WriteLine("\nНажмите любую клавишу для возврата в меню...");
            Console.ReadKey();
        }
    }
}

